# 3-1 연산자 종류
# 더하기
print(1+1) # 2

# 빼기
print(3-2) # 1

# 곱하기
print(5*2) # 10

# 나누기
print(6/3) # 2

# 거듭제곱
print(2**3) # 8

# 나머지 구하기
print(5%3)

# 몫 구하기
print(5//3)

print(5>3) # True
print(5<3) # False

print(3==3) # True
print(3==4) # Flase
print(3+4==7) #True

print(3!=2) # True
print(not (3 != 1)) # not은 뒤의 값을 반대로 인식함

# and -> 둘 다 모두 참이어야 참이 출력
print((3>0) and (3<5)) # Ture
print((3>0) & ((5>3)))

# or -> 둘 중 하나만 참이어도 참이 출력
print((3>0) or (5<3))
print((3>0) | (5<3))
