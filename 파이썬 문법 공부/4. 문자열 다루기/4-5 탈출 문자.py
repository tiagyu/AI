# 탈출 문자
# \n : 줄바꿈
print('백문이 볼여일견\n백견이 불여일타')

# \" \' : 문장 내에서 따옴표처럼 사용 가능
print("저는 '티아규'이다.")
print('저는 "티아규"이다.')
print("저는 \"티아규\"이다.") # \ 탈출 문자자

# \\ : 문장 내에서 \
print(' C:\\Users\\yjroh\\Documents\\Python\\Self-Study>')

# \r : 커서를 맨 앞으로 이동
print('Red Apple\rPine')

# \b : 백스페이스 (한 글자 삭제)
print('Redd\bApple')

# \t : 탭
print('Red\tApple')